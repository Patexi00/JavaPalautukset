package org.example;

public class th3 {

    static void ympyra(double sade){
        double pintaAla = Math.PI * Math.pow(sade, 2);
        System.out.println(pintaAla);
    }

    static void nelio(double sivuP){
        double pintaAla = Math.pow(sivuP, 2);
        System.out.println(pintaAla);
    }

    static void kolmio(double kanta, double korkeus){
        double pintaAla = kanta * korkeus / 2;
        System.out.println(pintaAla);
    }
}
